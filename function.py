def rata2(*angka):
    jumlah = 0
    for data in angka:
        jumlah += data
    else:
        average = jumlah / len(angka)
    print(f'Nilai rata-rata dari {(angka)} adalah {average}')

def total(*angka):
    jumlah = 0
    for data in angka:
        jumlah += data
    print(f'Total dari {(angka)} adalah {jumlah}')